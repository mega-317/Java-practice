package java_ex100;

import java.util.Scanner;

public class ex100_34 {
	public static void main(String[] args) {
		Scanner scan = new Scanner(System.in);
		while(true) {
			int input = scan.nextInt();
			if (input == 0) {
				System.out.println(input);
				break;
			}
			else {
				System.out.println(input);
			}
		}
	}
}
